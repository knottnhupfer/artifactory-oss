    ArtifactoryVersionProvider(String version) {
        this.version = version;
        this.revision = getRevisionFromVersion(version);
    }

    /**
     * The formula for version to revision is {Major}{Minor}{Patch}{Revision}
     * Major, Minor and Patch are 2 digits.
     * Revision is 900 for release, 9xx for customer patches (shouldn't be used!) and [0-8]xx for milestones.
     * examples:
     * 6.1.23        -> (0)60123900
     * 4.15.6-m001   -> (0)41506001
     */
    public static int getRevisionFromVersion(String version) {
        Matcher matcher = VERSION_PATTERN.matcher(version);
        if (matcher.matches()) {
            int major = Integer.parseInt(matcher.group("major"));
            int minor = Integer.parseInt(matcher.group("minor"));
            int patch = Integer.parseInt(matcher.group("patch"));
            String revString = matcher.group("rev");
            int rev = (revString == null) ? RELEASE_REVISION : getRevisionExtension(revString);
            return Integer.parseInt(String.format("%02d%02d%02d%03d", major, minor, patch, rev));
        }
        throw new IllegalArgumentException("Unable to parse version " + version);
    }

    private static int getRevisionExtension(String revString) {
        return revString.startsWith("m") ? Integer.parseInt(revString.substring(1)) :
                RELEASE_REVISION + Integer.parseInt(revString.substring(1));
    }

    public ArtifactoryVersion get() {
        return new ArtifactoryVersionImpl(version, revision, this);
    }

    public static ArtifactoryVersion get(String version, long revision) {
        ArtifactoryVersion artifactoryVersion = get(version);
        if (artifactoryVersion == null) {
            artifactoryVersion = new ArtifactoryVersionImpl(version, revision, null);
        }
        return artifactoryVersion;
    }

    public static ArtifactoryVersion get(String version) {
        for (ArtifactoryVersionProvider artifactoryVersionProvider : values()) {
            if (artifactoryVersionProvider.version.equals(version)) {
                return artifactoryVersionProvider.get();
            }
        }
        return null;
    }

